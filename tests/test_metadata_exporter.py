class TestGetOutputPath(unittest.TestCase):
    def test_series_path(self):
        path = get_output_path('/export', '三月的狮子', 'ABC123')
        self.assertTrue(path.startswith('/export'))
        self.assertIn('三月的狮子_ABC123', path)

    def test_book_path(self):
        path = get_output_path('/export', '三月的狮子', 'ABC123',
                               '第1卷', 'DEF456')
        self.assertIn('三月的狮子_ABC123', path)
        self.assertIn('第1卷_DEF456', path)

    def test_special_chars_sanitized(self):
        path = get_output_path('/export', 'a:b/c', 'ID1', 'v<1>', 'ID2')
        self.assertNotIn(':', path)
        self.assertNotIn('<', path)
        self.assertNotIn('>', path)


