class TestSanitizeFilename(unittest.TestCase):
    def test_illegal_chars_replaced(self):
        self.assertEqual(sanitize_filename('test:file'), 'test_file')
        self.assertEqual(sanitize_filename('a<b>c"d/e\\f|g?h*i'), 'a_b_c_d_e_f_g_h_i')

    def test_strips_leading_trailing_spaces_and_dots(self):
        self.assertEqual(sanitize_filename('  hello  '), 'hello')
        self.assertEqual(sanitize_filename('...test...'), 'test')
        self.assertEqual(sanitize_filename(' .mixed. '), 'mixed')

    def test_collapse_consecutive_underscores(self):
        self.assertEqual(sanitize_filename('a__b___c'), 'a_b_c')

    def test_truncation(self):
        long_name = 'x' * 300
        result = sanitize_filename(long_name, max_length=200)
        self.assertEqual(len(result), 200)
        self.assertFalse(result.endswith('_'))

    def test_none_returns_untitled(self):
        self.assertEqual(sanitize_filename(None), 'untitled')

    def test_empty_returns_untitled(self):
        self.assertEqual(sanitize_filename(''), 'untitled')

    def test_chinese_chars_preserved(self):
        self.assertEqual(sanitize_filename('三月的狮子'), '三月的狮子')

    def test_underscore_only_after_cleanup(self):
        self.assertEqual(sanitize_filename('***'), '_')


